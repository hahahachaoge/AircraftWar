package cn.edu.scnu.application;

/**
 * 游戏模式枚举，用于定义和区分游戏的不同玩法模式。
 *
 * <p>该枚举为游戏提供三种可选的运行模式，每种模式对应不同的游戏规则、
 * 玩家数量和关卡体验。游戏启动时通过读取此枚举值来决定加载哪一套游戏逻辑。
 *
 * <ul>
 *   <li>{@link #SINGLE}  — 单人模式，单个玩家控制飞机与敌方作战</li>
 *   <li>{@link #DOUBLE}  — 双人合作模式，两名玩家在同一屏幕内协同作战</li>
 *   <li>{@link #TUTORIAL} — 教学关卡，引导玩家熟悉基本操作</li>
 * </ul>
 *
 * @author 黄彪骐、岳孝彬、丁俊哲
 */
public enum GameMode {
    /** 单人模式：单个玩家操控战机，独自面对所有敌机与Boss。 */
    SINGLE,

    /** 双人合作模式：两名玩家同屏配合，共享分数与生命资源。 */
    DOUBLE,

    /** 教学关：简化版关卡，配有文字提示，帮助新玩家快速上手。 */
    TUTORIAL
}
