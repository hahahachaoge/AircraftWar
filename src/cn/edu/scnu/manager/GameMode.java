package cn.edu.scnu.manager;

/**
 * 游戏模式枚举，用于区分不同的游戏玩法与难度体验。
 * <p>
 * 定义了三种游戏模式：
 * <ul>
 *   <li>{@link #SINGLE} — 单人模式，玩家独自对抗敌机；</li>
 *   <li>{@link #DOUBLE} — 双人模式，两名玩家合作或对战；</li>
 *   <li>{@link #TUTORIAL} — 教学模式，引导新玩家熟悉基本操作。</li>
 * </ul>
 * </p>
 *
 * @author 黄彪骐、岳孝彬、丁俊哲
 */
public enum GameMode {
    /**
     * 单人模式。
     * 仅一名玩家操控战机，应对逐渐增强的敌机波次，适合单人挑战。
     */
    SINGLE,

    /**
     * 双人模式。
     * 两名玩家在同一屏幕内分别操控各自战机，可协作对敌或互相对抗。
     */
    DOUBLE,

    /**
     * 教学模式。
     * 以较低的敌机强度和引导式流程，帮助新玩家快速上手游戏操作与机制。
     */
    TUTORIAL
}
